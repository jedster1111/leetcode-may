export type Letters =
  | "a"
  | "b"
  | "c"
  | "d"
  | "e"
  | "f"
  | "g"
  | "h"
  | "i"
  | "j"
  | "k"
  | "l"
  | "m"
  | "n"
  | "o"
  | "p"
  | "q"
  | "r"
  | "s"
  | "t"
  | "u"
  | "v"
  | "w"
  | "x"
  | "y"
  | "z";

export const lettersMap: { [K in Letters]: K } = {
  a: "a",
  b: "b",
  c: "c",
  d: "d",
  e: "e",
  f: "f",
  g: "g",
  h: "h",
  i: "i",
  j: "j",
  k: "k",
  l: "l",
  m: "m",
  n: "n",
  o: "o",
  p: "p",
  q: "q",
  r: "r",
  s: "s",
  t: "t",
  u: "u",
  v: "v",
  w: "w",
  x: "x",
  y: "y",
  z: "z",
};

export const isStringLowerCaseLetter = (letter: string): letter is Letters => {
  return letter in lettersMap;
};

export function assertStringLowerCaseLetter(
  letter: string
): asserts letter is Letters {
  if (!isStringLowerCaseLetter(letter))
    throw new Error(
      `String contained something that wasn't a lower case letter. Found "${letter}."`
    );
}

assertStringLowerCaseLetter("t");
